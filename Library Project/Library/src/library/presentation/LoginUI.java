    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        boolean authenticated = false;
        
        // Get info from fields & Create Login Class
        String user = userNameField.getText();
        String pass = passwordField.getText();
        Login login = new Login();
        if ((user.equals("")) || (pass.equals(""))) 
            System.out.println("Error. Fields cannot be empty.");
        else {
            // Update Fields & call Login Manager
            login.setUserName(user);
            login.setPassword(pass);
            
            // Authenticate the user
            AuthenticateMgr authenticateMgr = new AuthenticateMgr();
            authenticated = authenticateMgr.authenticate(login);
            
            // Open BookUI if user is authenticated
            if (authenticated) {
                JFrame bookFrame = new BookUI();
                bookFrame.setVisible(true);
                bookFrame.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
            }
            else
               System.out.println("Invalid User Name or Password.");
        }
    }//GEN-LAST:event_okButtonActionPerformed

